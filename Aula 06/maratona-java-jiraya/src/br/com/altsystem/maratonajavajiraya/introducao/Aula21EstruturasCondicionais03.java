package br.com.altsystem.maratonajavajiraya.introducao;

public class Aula21EstruturasCondicionais03 {
    public static void main(String[] args) {
        //Regra
        //Doar salario maior que 5000
        double salario = 3000;
        String mensagemDoar = "Eu vou doar 500 para minha esposa";
        String mensagemNaoDoar = "Estou quebrado";
        // Ternário = (condição) ? verdadeiro : falso
        String resultado = salario > 5000 ? mensagemDoar : mensagemNaoDoar;
        System.out.println(resultado);










    }
}
